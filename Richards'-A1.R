#posterior function
logpostR <- function(par){
  lambda <- exp(par[1])
  kappa <- exp(par[2])
  beta <- exp(par[3])
  sigma <- exp(par[4])
  
  loglik <- sum( dnorm( y - richardsode(time, lambda, kappa, beta, log = TRUE), 
                        mean = 0, sd = sigma, log = TRUE) )
  
  logprior <- sum(dnorm(par,0,1,log=TRUE)) #use N(0,1) prior
  
  logjacob <- sum(par)
  
  return(-loglik - logprior - logjacob)
}

